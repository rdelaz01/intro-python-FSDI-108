# Dog age converter 
# Get the dog's age from the user


print("----Dog Age Converter----")
age = int(input("Enter your dog's age in human years: ")) #ask user to enter age in "human" years
dog_age = age * 7 # multiply the age provide by 7 to convert dog years

print(f"your dog is {dog_age} years old in dog years!") # dog age converted 